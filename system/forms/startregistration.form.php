<p>
	<b>Please note that you require the following documents to complete your application:</b>
</p>
<ul>
	<li>You need to pay the application fee of K150 this must be deposited in the National Institute of Public Administration ZANACO Bank Account No. 1067654300160 at any of their branches countrywide. For foreign students you need to make a bank draft of US$50 in the name of National Institute of Public Administration and scan a copy and attach it to the online application.</li>
	<li>You need to scan the deposit slip for the application fee and attach it as part of your online application. </li>
	<li>You need to scan and attach certified copies of your certificate or statement of results. <b>Please note your application will not be considered if you do not attach this document</b></li>
	<li>If you have obtained academic or professional training after leaving school (eg diploma) and want them to be used to support your application then you should scan and attach certified copies of your diploma certificates.</li>
	<li>You need to scan and attach either a copy of your passport or National Registration Card (NRC) or any form of identification</li>
	<li>You need to scan and attach a letter from your sponsor confirming that they are willing to sponsor you for the course at National Institute of Public Administration</li>
</ul>

<p>You will be required to create a password which you need to remember and use during 
the application process and use it to check whether your application is successful or not. 
A username will be automatically created and will be shown on the screen when you complete 
the application or log out during the application process. You will also need an email 
address that will be used to communicate to you. If you forget your password you can 
email us and a new password will be created and sent to your email box. If you wish to 
contact the University concerning this application you can email us on applications@nipa.ac.zm</p>

<p>Your application will not be considered unless you submit all required documents.</p>

